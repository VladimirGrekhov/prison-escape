// Prison Escape — общая топология поля. Работает и в браузере (глобальные имена),
// и в Node-сервере (module.exports). Чистые данные/хелперы, без canvas.
(function (root, factory) {
  const api = factory();
  if (typeof module !== 'undefined' && module.exports) module.exports = api;
  else Object.assign(root, api);
})(typeof self !== 'undefined' ? self : this, function () {
  // Главная петля по периметру креста (56 клеток). `.reverse()` — против часовой.
  const TRACK = [
    [3,10],[3,11],[4,11],[5,11],[6,11],[7,11],[8,11],[9,11],
    [9,12],[9,13],[9,14],[9,15],[9,16],[9,17],
    [10,17],
    [11,17],[11,16],[11,15],[11,14],[11,13],[11,12],[11,11],
    [12,11],[13,11],[14,11],[15,11],[16,11],[17,11],
    [17,10],
    [17,9],[16,9],[15,9],[14,9],[13,9],[12,9],[11,9],
    [11,8],[11,7],[11,6],[11,5],[11,4],[11,3],
    [10,3],
    [9,3],[9,4],[9,5],[9,6],[9,7],[9,8],[9,9],
    [8,9],[7,9],[6,9],[5,9],[4,9],[3,9],
  ].reverse();
  const TRACK_KEY = TRACK.map(([r, c]) => `${r},${c}`);
  const trackIndexOf = (r, c) => TRACK_KEY.indexOf(`${r},${c}`);

  // Клетка-вход на маршрут для каждого места.
  const ENTRY = [
    trackIndexOf(3, 10),   // место 0 — верх
    trackIndexOf(10, 17),  // место 1 — право
    trackIndexOf(10, 3),   // место 2 — лево
    trackIndexOf(17, 10),  // место 3 — низ
  ];

  // Домашняя дорожка (I→V→центр) каждого места, от кончика луча к центру.
  const HOME_LANE = [
    [[4,10],[5,10],[6,10],[7,10],[8,10],[9,10],[10,10]],
    [[10,16],[10,15],[10,14],[10,13],[10,12],[10,11],[10,10]],
    [[10,4],[10,5],[10,6],[10,7],[10,8],[10,9],[10,10]],
    [[16,10],[15,10],[14,10],[13,10],[12,10],[11,10],[10,10]],
  ];

  const TRACK_MAIN = TRACK.length;
  const LANE_LEN = HOME_LANE[0].length;
  // progress 0 = вход (рисуется на «Х»); далее петля; progress 56 = снова вход
  // (обязательная остановка перед домом); затем дорожка.
  const MAX_PROGRESS = TRACK_MAIN + LANE_LEN;

  // Конец дома — индекс клетки дорожки (везде «V», h*.4), дальше которой фишка
  // не идёт. Перескакивать нельзя: ход, уводящий дальше, запрещён; точное
  // попадание = финиш. Клетка-стрелка и центр — декоративные.
  const HOME_END = [4, 4, 4, 4];
  const maxProgressFor = (seat) => TRACK_MAIN + 1 + HOME_END[seat];

  // Кружок «Х» (вход в дом) каждого места — стартовая позиция (progress 0).
  const X_GRID = [[2, 9], [9, 18], [11, 2], [18, 11]];

  // Безопасные клетки (нельзя срубить): кружки «Х» + карманы БМ (ниже).
  // Входы — обычные клетки маршрута, на них рубится как везде (фишку на «Х»
  // защищает не клетка, а проверка progress 0 в движке).
  const SAFE = new Set([
    '2,9', '9,18', '18,11', '11,2',
  ]);

  // nx/ny — направление сдвига кружка БМ наружу (для отрисовки/центра кармана).
  const BM_CELLS = [
    {r:8,  c:6,  nx:0,  ny:-1}, {r:12, c:6,  nx:0,  ny:1},
    {r:8,  c:14, nx:0,  ny:-1}, {r:12, c:14, nx:0,  ny:1},
    {r:6,  c:8,  nx:-1, ny:0},  {r:6,  c:12, nx:1,  ny:0},
    {r:14, c:8,  nx:-1, ny:0},  {r:14, c:12, nx:1,  ny:0},
  ];
  // Сопоставление БМ ↔ соседняя клетка маршрута (для съезда «на БМ»).
  const BM_BY_TRACK = {};
  BM_CELLS.forEach((b) => {
    [[b.r-1, b.c], [b.r+1, b.c], [b.r, b.c-1], [b.r, b.c+1]].forEach(([r, c]) => {
      const idx = trackIndexOf(r, c);
      if (idx >= 0) BM_BY_TRACK[idx] = b;
    });
  });
  BM_CELLS.forEach((b) => SAFE.add(`${b.r},${b.c}`)); // карманы БМ безопасны

  // Экспресс-клетки: стоя на одной и выбросив 1, прыжок на следующую (по кругу);
  // выбросив 3 — на противоположную (в обе стороны).
  const EXPRESS = [6, 20, 34, 48];
  const EXPRESS_NEXT = { 6: 20, 20: 34, 34: 48, 48: 6 };
  const EXPRESS_ACROSS = { 6: 34, 34: 6, 20: 48, 48: 20 };

  return {
    TRACK, TRACK_KEY, trackIndexOf, ENTRY, HOME_LANE,
    TRACK_MAIN, LANE_LEN, MAX_PROGRESS, HOME_END, maxProgressFor, X_GRID, SAFE,
    BM_CELLS, BM_BY_TRACK, EXPRESS, EXPRESS_NEXT, EXPRESS_ACROSS,
  };
});
